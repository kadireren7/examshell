#include <unistd.h>
static void	put(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	if (argc == 2)
	{
		i = 0;
		while (argv[1][i])
		{
			char	c = argv[1][i];

			if (c >= 'a' && c <= 'z')
				c = 'a' + 'z' - c;
			else if (c >= 'A' && c <= 'Z')
				c = 'A' + 'Z' - c;
			put(c);
			i++;
		}
	}
	put('\n');
	return (0);
}
