#include <stdio.h>

int	ft_atoi(const char *str);

int	main(void)
{
	const char	*t[] = {"42", "  42", "-42", "+42", "0", "  -0",
		"2147483647", "-2147483648", "123abc", "abc123", "", "   ",
		"  \t\n 42", "--42", "+-42", "  +1234ab567", "007", "-0012"};
	size_t		i;

	i = 0;
	while (i < sizeof(t) / sizeof(t[0]))
	{
		printf("%d\n", ft_atoi(t[i]));
		i++;
	}
	return (0);
}
