#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dst;
	int		len;
	int		i;

	len = 0;
	while (src[len])
		len++;
	dst = malloc((unsigned long)len + 1);
	if (!dst)
		return (0);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
